function help(level){
	switch(level) {
		case 0:
			alert("Click here to get some tips about the questions...");
			break;
		case 1:
			alert("Something is the answer...");
			break;
		case 2:
			alert("Selecting text may help...")
			break;
		case 3:
			alert("Words appear when you hover over them...");
			break;
		case 4:
			alert("Did you notice any difference in the adress bar?");
		break;
		case 5:
			alert("Perhaps the previous answers form a message...")
	}
}
